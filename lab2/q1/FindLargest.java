class FindLargest
{

public static void main(String args[])
{

int a = 10, b = 18, c = 5;

if(a>b && a>c)
{
System.out.println(a+" is largest among the three");
}

else if(b>a && b>c)
{
System.out.println(b+" is largest among the three");
}

else
{
System.out.println(c+" is largest among the three");
}

}

}